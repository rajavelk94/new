package org.seleniumproject;

public class Day4forloop {

	public static void main(String[] args) {
		
		
		//asending order
		for(int i=0;i<=10;i++) {
			
			System.out.println(i);
		}
		System.out.println(" ");
		
		//desending order
		for(int j=10;j>=1;j--) {
			System.out.println(j);
			
		}

	}

}
